'''Ler uma temperatura em graus centígrados e apresentá-la convertida em graus
. , - , 9.c+160 Fahrenheit. A formula de conversão e:F = onde F e a temperatura em
Fahrenheit e C é a temperatura em centígrados.'''

grausCentigrados = float(input("Digite a temperatura em graus centígrados: "))
Fahrenheit = (9 * grausCentigrados + 160) /5

print(f"A temperatura em Fahrenheit é: {Fahrenheit}")