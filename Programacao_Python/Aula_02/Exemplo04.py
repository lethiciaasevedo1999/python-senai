#Trabalhando com operadores lógicos e inputs

#Soma
valor_01 = float(input("Digite um valor: "))
valor_02 = float(input("Digite outro valor: "))

soma = valor_01 + valor_02
print(f"O resultado é: {soma} ")

#Exponenciação
exponenciacao = valor_01 ** valor_02
print(f"O resultado é: {exponenciacao} ")

#Divisão
#divisao = valor_01 // valor_02 --> Para retornar a divisão inteira
divisao = valor_01 / valor_02
print(f"O resultado é: {divisao} ")

#Resto
resto = valor_01 % valor_02
print(f"O resultado é: {resto} ")